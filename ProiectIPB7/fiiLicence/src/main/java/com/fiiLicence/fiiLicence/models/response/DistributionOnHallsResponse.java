package com.fiiLicence.fiiLicence.models.response;

public class DistributionOnHallsResponse {
    private String numeStudent;
    private String prenumeStudent;
    private String sala;
    private int idComisie;
    private String oraDeInceput;
    private String oraDeSfarsit;

    public String getNumeStudent() {
        return numeStudent;
    }

    public void setNumeStudent(String numeStudent) {
        this.numeStudent = numeStudent;
    }

    public String getPrenumeStudent() {
        return prenumeStudent;
    }

    public void setPrenumeStudent(String prenumeStudent) {
        this.prenumeStudent = prenumeStudent;
    }

    public String getSala() {
        return sala;
    }

    public void setSala(String sala) {
        this.sala = sala;
    }

    public int getIdComisie() {
        return idComisie;
    }

    public void setIdComisie(int idComisie) {
        this.idComisie = idComisie;
    }

    public String getOraDeInceput() {
        return oraDeInceput;
    }

    public void setOraDeInceput(String oraDeInceput) {
        this.oraDeInceput = oraDeInceput;
    }

    public String getOraDeSfarsit() {
        return oraDeSfarsit;
    }

    public void setOraDeSfarsit(String oraDeSfarsit) {
        this.oraDeSfarsit = oraDeSfarsit;
    }
}
